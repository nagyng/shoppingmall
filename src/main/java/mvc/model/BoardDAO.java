	public int getListCount(String items, String text) {
		Connection conn = null;
		PreparedStatement pstmt = null;
		ResultSet rs = null;

		int x = 0;

		String sql;

		/*검색구분 x, 검색문자열x => 전체검색*/
		if (items == null && text == null)
			sql = "select  count(*) from board";
		else
			sql = "SELECT   count(*) FROM board where " + items + " like '%" + text + "%'";
		
		try {
			conn = DBConnection.getConnection();
			pstmt = conn.prepareStatement(sql);
			rs = pstmt.executeQuery();

			if (rs.next()) 
				x = rs.getInt(1);
			
		} catch (Exception ex) {
			System.out.println("getListCount() 예외발생: " + ex);
		} finally {			
			try {				
				if (rs != null) 
					rs.close();							
				if (pstmt != null) 
					pstmt.close();				
				if (conn != null) 
					conn.close();												
			} catch (Exception ex) {
				throw new RuntimeException(ex.getMessage());
			}		
		}		
		return x;
	}
	//board 테이블의 레코드 가져오기	
		//매개변수: 현재 페이지 번호, 한 페이지 당 행 개수, 검색 분류(작성자,제목,..), 검색할 문자열 
	public ArrayList<BoardDTO> getBoardList(int page, int limit, String items, String text) {
		Connection conn = null;
		PreparedStatement pstmt = null;
		ResultSet rs = null;

		/*검색 조건에 따른 총 건수 계산*/
		int total_record = getListCount(items, text );
		/*시작페이지*/
		int start = (page - 1) * limit;
		int index = start + 1;

		String sql;

		/*(검색 내용 없음) 게시물 전체 검색*/
		if (items == null && text == null)
			sql = "select * from board ORDER BY num DESC";		//최신글부터
		else
			sql = "SELECT  * FROM board where " + items + " like '%" + text + "%' ORDER BY num DESC ";

		/*리스트에 담기 */
		ArrayList<BoardDTO> list = new ArrayList<BoardDTO>();
	
		try {
			/*데이터베이스에 연결*/
			conn = DBConnection.getConnection();
			

			/*
			 * TYPE_SCROLL_INSESITIVE	:	ResultSet 커서를 원하는 행으로 이동 시 변경된 행의 값을 미반영 시킨다 (SENSITIVE = 반영)
			 * TYPE_SCROLL_SENSITIVE	:	ResultSet 커서를 원하는 행으로 이동 시 변경된 행의 값을 반영 시킨다
			 * CONCUR_UPDATABLE			:	현재 행의 값을 변경 가능하도록 하는 옵션	(READ_ONLY = 변경 불가능)
			 * CONCUR_READ_ONLY			:	현재 행의 값을 변경하지 못하게 하는 옵션 
			 * */
			pstmt = conn.prepareStatement(sql,ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
			rs = pstmt.executeQuery();
			/* */
			while (rs.absolute(index)) {
				BoardDTO board = new BoardDTO();
				board.setNum(rs.getInt("num"));
				board.setId(rs.getString("id"));
				board.setName(rs.getString("name"));
				board.setSubject(rs.getString("subject"));
				board.setContent(rs.getString("content"));
				board.setRegdate(rs.getTimestamp("regdate"));				//날짜 가져오기는 .getTimestamp()
				board.setUpdatedate(rs.getTimestamp("updatedate"));
				board.setHit(rs.getInt("hit"));
				board.setIp(rs.getString("ip"));
				
				//ArrayList에 게시물을 추가
				list.add(board);
				
			
				/*조회된 총 건 수와 비교하여 index값을 증가시킨다 */
				if (index < (start + limit) && index <= total_record) {
					index++;
				}else {
					break;
				}
			}
			return list;
		} catch (Exception ex) {
			System.out.println("getBoardList() 예외발생 : " + ex);
		} finally {
			try {
				if (rs != null) 
					rs.close();							
				if (pstmt != null) 
					pstmt.close();				
				if (conn != null) 
					conn.close();
			} catch (Exception ex) {
				throw new RuntimeException(ex.getMessage());
			}			
		}
		return null;
	}
